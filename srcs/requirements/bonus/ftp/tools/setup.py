import os
from FTP import FTP

def setup():
	ftp = FTP(os.environ['FTP_USER'],
		os.environ['FTP_PASSWORD'], os.environ['FTP_DIR'])
	ftp.install()
	ftp.configure()
	ftp.start()

setup()
